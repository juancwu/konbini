package utils

// CombineBytes combines two byte arrays into one. The byte array b is appended after a.
func CombineBytes(a []byte, b []byte) []byte {
	c := make([]byte, len(a)+len(b))
	copy(c, a)
	copy(c[len(a):], b)
	return c
}

// Compares if two slices are the equal in value
func EqualBytes(a []byte, b []byte) bool {
	if len(a) != len(b) {
		return false
	}

	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}

	return true
}
